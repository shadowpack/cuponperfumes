<b>¿Cómo comprar un cupón?</b><br>
<br>
1.	Ingresa a nuestro sitio.<br>
2.	Elige el descuento de tu interés. <br>
3.	Presiona "comprar", llena tus datos, elige tu forma de pago, y ¡listo!<br>
<br>
<b>¿Cómo obtengo el cupón para usar en el comercio?</b><br>
<br>
Aproximadamente una hora después de terminado tu proceso de compra, recibirás un correo con un link para ver una versión imprimible de tu cupón. Úsalo siguiendo los horarios y condiciones .
Además, si quieres ahorrar tinta y papel, puedes simplemente mostrar tu cupón desde tu smartphone y así estarás ayudando a conservar el medioambiente.<br>
<br>
<b>¿Se necesita un mínimo de compradores para que se haga efectivo el cupón?</b><br>
<br>
No. A diferencia de otros sitios de descuentos, en cuponperfumes.cl no exigimos un mínimo de cupones vendidos.<br>
<br>
<b>¿Cuándo puedo usar mi cupón, una vez comprado?</b><br>
<br>
Si ya recibiste la versión imprimible del cupón, imprímelo y ven a cobrarlo a nuestras oficinas  dentro del plazo señalado en tu compra. Si prefieres no imprimir para ahorrar tinta y papel o para no contaminar, puedes simplemente mostrar tu cupón desde tu smartphone (Iphone, Blackberry, Galaxy, etc.). Pero atento a las restricciones de tiempo.<br>
Si compré un cupón, ¿Puedo regalarlo?<br>
<br>
Por supuesto, nuestros cupones no son nominativos y son una perfecta alternativa de regalo para amigos, familiares o personas especiales.<br>
<br>
<b>Si me arrepiento de una compra, ¿Puedo recibir reembolso?</b><br>
<br>
No podemos hacer reembolsos. Si tienes razones especiales, acércate y lo analizaremos.<br>
<br>
<b>¿Cuán segura es mi información?</b><br>
<br>
La información de tu tarjeta de crédito o débito es administrada sólo por los medios de pago. CuponPerfumes.cl no tiene acceso a tu información bancaria.<br>
<br>
<b>¿Será usado mi mail para hacerme llegar publicidad adicional?</b><br>
<br>
No, sabemos lo incómodo que es ser bombardeado por correos inútiles. Sólo te enviaremos las ofertas diarias e información necesaria en caso de realizar una compra. Tampoco compartimos tu información con otras empresas, así que puedes estar tranquilo.<br>
